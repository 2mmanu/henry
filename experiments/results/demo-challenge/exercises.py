# Create a function that returns the sum of two numbers

def sum_two_numbers(a, b):
    return a + b

# Exercise 2 (Intermediate - 10 points)
# Create a function that takes a list of numbers as a parameter, and returns another list only with the even numbers

def filter_even_numbers(list_numbers):
    return [num for num in list_numbers if num % 2 == 0]

# Exercise 3 (Advanced - 15 points)
# Create a function that takes a list of numbers and sorts it in ascending order.

def sort_numbers(list_numbers):
    return sorted(list_numbers)




